<p>A new and popular tool in CSS currently is Flexbox, which Nick Schaferhoff of Torquemag.io refers to as “an efficient way to arrange, organize, and size website elements to create highly adaptive designs.”
    <br><br>Flexbox uses containers (the ‘boxes’) that can expand (or ‘flex’) along two different axes, while providing a rule set that allows the elements inside to be arranged and sized in different ways.
    The simplicity and elastic nature of Flexbox makes it a good choice for groups of elements where you don’t need to get very specific with layout and can’t predict future changes. “Sets of form fields, icons, or other information can be easily laid out and allowed to be flexible without needing to carefully set the sizing on each item”, writes Rachel Andrew at smashingmagazine.com.
    <br><br>An alternative solution (or one that can be used in conjunction) is CSS Grid. “If you want to accurately control the position of items within a layout, CSS Grid is the way to go”, asserts Anna Monus at tutsplus.com. This can be very useful if you have a specific layout you want, but the trade off is losing the very forgiving nature of a Flexbox layout.
    Neither method is exclusive of the other. They both have their strengths and weaknesses, but they can be used in concert to balance out each other. Flexbox has a slight advantage due to being a more effective way to center items in a container and a little bit more browser support, but the fact that Grid allows for better two dimensional arrangement of elements and more specificity in layout are a couple of marks in its favor as well.
    <br><br>Flexbox is currently used more frequently due to having been around longer and having a wide range of applications, so it is likely to be used whether CSS Grid is also used or not. Ultimately, as with almost every other aspect of website design, a balance must be struck between functionality and appearance, with Flexbox as a powerful tool for balancing the two.


