<p>There are lots of options for getting information out to people on the internet, but two of the most popular are responsive web sites and mobile apps. When I client is deciding whether to do one, the other, or both, a number of factors need to be weighed.
    One such factor is functionality.  <br><br>A mobile app “can be designed with a lot of elaborate functions, based on advanced gestures”, writes Nicole Kohler of webfx.com. Clearbridgemobile.com adds that “mobile apps can access and use built-in device features” in a way that even a responsive website may not be able to.
    But another is coding effort and cost. <br><br>Nick Babich of Adobe.com cautions, “If your product team wants to release an app for both iOS and Android, then they will have to write a code for each operating system.” According to Nicole Kohler of web fax.com, “if you have a time-sensitive service of any kind, a mobile site is a great way to give your customers a fast way to perform actions without getting frustrated and leaving your site for a competitors.”
    <br><br>Apps have the advantage of not necessarily needing internet service: “Once the app has been downloaded, its data can be stored directly on a device”, writes Nick Babich at Adobe.com.
    <br><br>Ultimately, the decision should come down to the goals of your client and their resources. A client with limited resources might just need to deliver information or perform basic functions, and a responsive app could be fine. Another client with deep pockets and a need for broad or highly specific functionality might benefit from an app. A developer needs to be able to balance the needs of the client against the resources available and help the client arrive at the most effective solution for their situation.
